namespace MUFramework
{
    /// <summary>
    /// 界面被覆盖时的行为枚举
    /// </summary>
    public enum CoverdBehavior
    {
        /// <summary>
        /// 保持正常显示状态（不暂停也不隐藏）
        /// </summary>
        Normal,

        /// <summary>
        /// 暂停
        /// </summary>
        Pause,

        /// <summary>
        /// 隐藏
        /// </summary>
        Hide,
    }
}
